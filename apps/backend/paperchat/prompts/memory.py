from __future__ import annotations


def build_session_memory_summary_prompt(*, existing_summary: str = "", conversation_text: str) -> str:
    return (
        "你是一位专门负责“会话长期记忆整理”的研究助手。"
        "你的任务是把聊天历史压缩为后续对话可复用的长期摘要，供下一轮对话作为背景使用。"
        "\n\n"
        "## 摘要目标\n"
        "请将新增聊天历史整合进已有摘要，形成一段更完整但仍然精炼的会话记忆。"
        "这段记忆不是给最终用户直接看的，而是给后续模型继续对话时参考的。"
        "\n\n"
        "## 必须保留的信息\n"
        "1. 用户当前研究目标或真实意图\n"
        "2. 已明确的研究对象、主题边界、约束条件\n"
        "3. 已确认的重要事实、判断、结论或方案倾向\n"
        "4. 用户偏好的表达方式、产出形式或使用场景\n"
        "5. 尚未解决、值得后续继续追问的问题\n"
        "\n"
        "## 必须删除的信息\n"
        "- 客套寒暄\n"
        "- 对当前任务没有帮助的重复表述\n"
        "- 逐句转写聊天记录\n"
        "- 无法确认真实性的推测\n"
        "\n"
        "## 输出要求\n"
        "- 输出简洁中文摘要\n"
        "- 优先使用 4 到 8 条项目符号列表\n"
        "- 每条尽量短，但要可复用\n"
        "- 不要加解释性前缀，不要输出“以下是摘要”这类套话\n"
        "- 如果新增内容与已有摘要冲突，以更新后的明确信息为准\n"
        "\n"
        f"已有摘要：\n{existing_summary or '（暂无）'}\n\n"
        f"新增历史：\n{conversation_text}"
    )
